    def analyze_issue(self, issue: Dict[str, Any]) -> Dict[str, Any]:
        """Issueå†…å®¹ã‚’è§£æã—ã¦å°‚é–€åˆ†é‡ã‚’åˆ¤å®š"""
        title = issue['title'].lower()
