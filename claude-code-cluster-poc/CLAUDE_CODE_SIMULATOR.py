class ClusterSimulator:
    """Claude Code Clusterã®ã‚·ãƒŸãƒ¥ãƒ¬ãƒ¼ã‚¿ãƒ¼"""
    
    def __init__(self):
        self.claude_mock = MockClaudeCodeCLI()
