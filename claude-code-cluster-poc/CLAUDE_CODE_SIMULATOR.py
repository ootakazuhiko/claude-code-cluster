    def select_agent(self, requirements: List[str]) -> str:
        """è¦ä»¶ã«åŸºã¥ã„ã¦æœ€é©ãªã‚¨ãƒ¼ã‚¸ã‚§ãƒ³ãƒˆã‚’é¸æŠ"""
        best_agent = None
